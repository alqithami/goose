#!/usr/bin/env python3
"""MCP server for inspecting EcoFair-CH-MARL experiment results.

This extension is intentionally lightweight. It reads CSV outputs generated by
https://github.com/alqithami/EcoFairCHAMRL and exposes compact summaries to Goose.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

import pandas as pd
from mcp.server.fastmcp import FastMCP


DEFAULT_RESULTS_DIR = Path.cwd()
RESULTS_DIR = DEFAULT_RESULTS_DIR

mcp = FastMCP("chmarl-results")


def _safe_results_dir() -> Path:
    """Return the configured results directory, raising a clear error if missing."""
    if not RESULTS_DIR.exists():
        raise FileNotFoundError(f"CHMARL results directory does not exist: {RESULTS_DIR}")
    if not RESULTS_DIR.is_dir():
        raise NotADirectoryError(f"CHMARL results path is not a directory: {RESULTS_DIR}")
    return RESULTS_DIR


def _csv_files() -> list[Path]:
    """Return CHMARL-style CSV result files from the configured directory."""
    results_dir = _safe_results_dir()
    patterns = [
        "results_*.csv",
        "fairness_metrics_*.csv",
        "training_fairness_metrics_*.csv",
        "ppo_only_test_returns.csv",
    ]
    files: list[Path] = []
    for pattern in patterns:
        files.extend(results_dir.glob(pattern))
    return sorted(set(files))


def _numeric_summary(df: pd.DataFrame) -> dict[str, dict[str, float | int | None]]:
    """Build a compact descriptive summary for numeric columns."""
    numeric = df.select_dtypes(include="number")
    summary: dict[str, dict[str, float | int | None]] = {}
    for column in numeric.columns:
        series = numeric[column].dropna()
        if series.empty:
            summary[column] = {
                "count": 0,
                "mean": None,
                "std": None,
                "min": None,
                "max": None,
                "last": None,
            }
            continue
        summary[column] = {
            "count": int(series.count()),
            "mean": float(series.mean()),
            "std": None if pd.isna(series.std()) else float(series.std()),
            "min": float(series.min()),
            "max": float(series.max()),
            "last": float(series.iloc[-1]),
        }
    return summary


def _summarize_file(path: Path) -> dict[str, Any]:
    """Read and summarize one CSV file."""
    df = pd.read_csv(path)
    return {
        "file": path.name,
        "rows": int(len(df)),
        "columns": list(df.columns),
        "numeric_summary": _numeric_summary(df),
    }


@mcp.tool()
def list_chmarl_result_files() -> str:
    """List CHMARL CSV result files in the configured results directory."""
    files = _csv_files()
    payload = {
        "results_dir": str(_safe_results_dir()),
        "file_count": len(files),
        "files": [file.name for file in files],
    }
    return json.dumps(payload, indent=2)


@mcp.tool()
def summarize_chmarl_file(filename: str) -> str:
    """Summarize a single CHMARL CSV result file by filename."""
    requested = Path(filename).name
    candidates = {file.name: file for file in _csv_files()}
    if requested not in candidates:
        payload = {
            "error": f"File not found in CHMARL results directory: {requested}",
            "available_files": sorted(candidates),
        }
        return json.dumps(payload, indent=2)
    return json.dumps(_summarize_file(candidates[requested]), indent=2)


@mcp.tool()
def summarize_chmarl_results() -> str:
    """Summarize all CHMARL CSV result files in the configured results directory."""
    files = _csv_files()
    summaries = [_summarize_file(file) for file in files]
    payload = {
        "results_dir": str(_safe_results_dir()),
        "file_count": len(files),
        "summaries": summaries,
    }
    return json.dumps(payload, indent=2)


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="CHMARL results MCP server")
    parser.add_argument(
        "--results-dir",
        default=str(DEFAULT_RESULTS_DIR),
        help="Directory containing EcoFair-CH-MARL CSV result files.",
    )
    return parser.parse_args()


def main() -> None:
    global RESULTS_DIR
    args = _parse_args()
    RESULTS_DIR = Path(args.results_dir).expanduser().resolve()
    mcp.run()


if __name__ == "__main__":
    main()
